package colecao_dvds;

public class Autor {
	protected String nome;
	protected String atua;
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setAtua(String atua) {
		this.atua = atua;
	}
	
	public String getAtua() {
		return atua;
	}
}
